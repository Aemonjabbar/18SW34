class Task2b{
	public static void main(String args[]){
	int x=6, y=12, z=18;
	System.out.println("x<y & y>z="+(x<y & y>z));
	System.out.println("x>y | y>z="+(x>y | y>z));
	System.out.println("x>y ^ y<z="+(x>y ^ y<z));
	System.out.println("x<y && y>z="+(x<y && y>z));
	System.out.println("x>y	|| y>z="+(x>y || y>z));
	System.out.println("!(x<y)"+(!(x<y)));
	}
}